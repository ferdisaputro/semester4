void main() {
  // Looping pertama: menghitung maju
  print('LOOPING PERTAMA');
  int i = 2;
  while (i <= 20) {
    print('$i - I love coding');
    i += 2;
  }

  // Looping kedua: menghitung mundur
  print('\nLOOPING KEDUA');
  int j = 20;
  while (j >= 2) {
    print('$j - I will become a mobile developer');
    j -= 2;
  }
}
